function record = metadata_from_options(prefix, options, record)

    if ~exist('record', 'var')
        record = hdng.utilities.Dictionary();
    end

    option_names = fieldnames(options);
    
    for index=1:numel(option_names)
        name = option_names{index};
        
        record([prefix name]) = hdng.experiments.Value.from(options.(name)).label;
    end
end
