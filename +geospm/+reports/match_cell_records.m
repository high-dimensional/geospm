function [grid_cells] = match_cell_records(grid_cells, selector)
    
    for index=1:numel(grid_cells)
        cell_records = grid_cells{index};
        
        if isempty(cell_records)
            continue;
        end

        cell_records = cell_records.select(selector);

        grid_cells{index} = cell_records;
    end
end
