function cmd = create_cmd(func, identifier, directory, arguments, options)
    cmd = struct();
    cmd.func = func2str(func);
    cmd.identifier = identifier;
    cmd.directory = directory;
    cmd.arguments = arguments;
    cmd.options = options;
end
