function run_cmd_from(command_file)
    cmd = load(command_file);
    optional_args = hdng.utilities.struct_to_name_value_sequence(cmd.options);
    cmd_function = str2func(cmd.func);
    cmd_function(cmd.arguments{:}, optional_args{:});
end
