function fwhm = fwhm_from_p_diameter(p, diameter, dimensions)
    
    if ~exist('dimensions', 'var')
        dimensions = 1;
    end
    
    stddev = geospm.utilities.stddev_from_p_diameter(p, diameter, dimensions);
    fwhm = geospm.utilities.fwhm_from_stddev(stddev);
end
