function diameter = diameter_from_p_fwhm(p, fwhm, dimensions)
    
    if ~exist('dimensions', 'var')
        dimensions = 1;
    end
    
    stddev = geospm.utilities.stddev_from_fwhm(fwhm);
    diameter = geospm.utilities.diameter_from_p_stddev(p, stddev, dimensions);
end
