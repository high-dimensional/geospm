function result = collection_from_wkt(file_path)
    
    result = hdng.wkt.WKTGeometryCollection.from_file(file_path);

    if ~isempty(result.errors)
        error('Collection.from_wkt(): Parse error(s).');
    end

    wkt_geometry = result.value;
    result = hdng.geometry.impl.collection_from_wkt_geometry(wkt_geometry);
end
