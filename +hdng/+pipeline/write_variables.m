function result = write_variables(...
            variable_value_list, ...
            interface, ...
            result)

    if ~exist('result', 'var')
        result = struct();
    end

    names = fieldnames(interface);

    for i=1:numel(names)
        name = names{i};
        variable = interface.(name);
        result.(name) = variable_value_list{variable.nth_variable};
    end
end
