function [variable] = constant(schedule, identifier, description, value, varargin)
    %constant Defines a variable with one or more constant values
        
    generator = hdng.experiments.ValueList.from(value, varargin{:});
    variable = hdng.experiments.Variable(schedule, identifier, generator, {}, 'description', description);
end
