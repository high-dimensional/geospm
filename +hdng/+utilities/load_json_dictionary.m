function result = load_json_dictionary(file_path)
    
    text = hdng.utilities.load_text(file_path);
    map = hdng.utilities.decode_json(text);
    
    result = hdng.utilities.Dictionary();
    
    keys = map.keys();
    
    for index=1:numel(keys)
        key = keys{index};
        result(key) = map(key);
    end
end
