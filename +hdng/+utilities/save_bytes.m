function save_bytes(bytes, file_path)

    [directory, ~, ~] = fileparts(file_path);

    [status,msg] = mkdir(directory);
    
    if status ~= 1
        error('save_bytes(): %s', msg);
    end
    
    h = fopen(file_path, "w", "n");
    fwrite(h, bytes, 'uint8');
	fclose(h);
end
