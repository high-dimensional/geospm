function parts = split_camelcase(string)
%split_camelcase Splits a string into separate components along case
%changes and whitespace
    tmp = regexprep(string, {'([a-z])([A-Z])' '([A-Z]+)([A-Z][a-z])'}, '$1 $2');
    parts = split(tmp);
end
