function [directory_paths, directory_tokens] = scan_directories(directory, name_pattern)

    [~, directory_paths] = hdng.utilities.list_files(directory);
    [directory_paths, directory_tokens] = hdng.utilities.match_file_paths_by_name(directory_paths, name_pattern);
end
