function save_cell_as_csv(filepath, cell_array)

    [~, ~, ext] = fileparts(filepath);

    if ~strcmpi(ext, '.csv')
        error('save_cell_as_csv: File name must specifies a suffix that is not equal to csv.');
    end

    writecell(cell_array,filepath)

end
