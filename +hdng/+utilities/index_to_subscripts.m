function result = index_to_subscripts(dimensions, index)

    N_dimensions = numel(dimensions);
    result = zeros(1, N_dimensions);

    for i=1:N_dimensions
        [outer, inner] = ind2sub(dimensions(i:end), index);
        result(i) = outer;
        index = inner;
    end
end
